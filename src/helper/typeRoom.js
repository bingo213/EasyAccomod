export default function typeOfRoom(type){
    if (type === 'phongtro')
        return "Phòng trọ";
    else if (type === 'chungcu')
        return "Chung cư";
    else if (type === 'nha_nguyencan')
        return "Nhà nguyên căn";
    else if (type === 'chungcu_nguyencan')
        return "Chung cư nguyên căn"
}
